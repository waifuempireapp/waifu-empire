// Map coordinate → nome univoco
export const PIXEL_NAMES = Object.fromEntries(_uniquePixels.map(p => [`${p.x}_${p.y}`, p.name]));

// Array completo con nomi unici (usato dal seed script)
export const UNIQUE_PIXELS = _uniquePixels;
